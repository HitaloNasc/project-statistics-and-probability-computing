
def valor_max(data):
    """
    Calcular o valor máximo dos dados
    Arg: 
        data: Dados formatados
    Return:
        valor máximo dos dados
    """
    return max(data)


def valor_min(data):
    """
    Cria o gráfico boxplot
    Arg: 
        data: Dados formatados
    Return:
        Criação gráfica do boxplot a partir dos dados inseridos
    """
    return min(data)
